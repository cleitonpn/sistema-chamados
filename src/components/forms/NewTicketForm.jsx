class DynamicAITemplateService {
  constructor() {
    this.templatesCollection = 'ai_templates';
    this.analyticsCollection = 'ai_analytics';
  }

  async loadAITemplates() {
    try {
      const templatesRef = collection(db, this.templatesCollection);
      const q = query(templatesRef, orderBy('confidence', 'desc'), limit(10));
      const snapshot = await getDocs(q);
      
      const templates = [];
      snapshot.forEach((doc) => {
        templates.push({ id: doc.id, ...doc.data() });
      });
      
      console.log('ü§ñ Templates IA carregados:', templates.length);
      return templates;
    } catch (error) {
      console.error('‚ùå Erro ao carregar templates IA:', error);
      return [];
    }
  }

  async analyzeAndUpdateTemplates() {
    try {
      console.log('üîç Iniciando an√°lise autom√°tica de chamados...');
      
      const thirtyDaysAgo = new Date();
      thirtyDaysAgo.setDate(thirtyDaysAgo.getDate() - 30);
      
      const ticketsRef = collection(db, 'tickets');
      const q = query(
        ticketsRef, 
        where('createdAt', '>=', thirtyDaysAgo),
        orderBy('createdAt', 'desc')
      );
      const snapshot = await getDocs(q);
      
      const tickets = [];
      snapshot.forEach((doc) => {
        tickets.push({ id: doc.id, ...doc.data() });
      });
      
      console.log('üìä Chamados analisados:', tickets.length);
      
      const patterns = {};
      tickets.forEach(ticket => {
        const key = `${ticket.area}_${ticket.tipo}`;
        if (!patterns[key]) {
          patterns[key] = {
            area: ticket.area,
            tipo: ticket.tipo,
            titles: [],
            descriptions: [],
            priorities: [],
            count: 0
          };
        }
        
        patterns[key].titles.push(ticket.titulo);
        patterns[key].descriptions.push(ticket.descricao);
        patterns[key].priorities.push(ticket.prioridade);
        patterns[key].count++;
      });
      
      const newTemplates = [];
      Object.entries(patterns).forEach(([key, pattern]) => {
        if (pattern.count >= 3) {
          const template = this.generateTemplateFromPattern(key, pattern);
          if (template) {
            newTemplates.push(template);
          }
        }
      });
      
      for (const template of newTemplates) {
        await this.saveTemplate(template);
      }
      
      console.log('‚úÖ Templates atualizados:', newTemplates.length);
      return newTemplates;
    } catch (error) {
      console.error('‚ùå Erro na an√°lise autom√°tica:', error);
      return [];
    }
  }

  generateTemplateFromPattern(key, pattern) {
    try {
      const mostCommonTitle = this.getMostCommonPhrase(pattern.titles);
      const mostCommonPriority = this.getMostCommon(pattern.priorities);
      const commonPhrases = this.extractCommonPhrases(pattern.descriptions);
      
      const template = {
        id: `auto_${key}_${Date.now()}`,
        area: pattern.area,
        tipo: pattern.tipo,
        titulo: mostCommonTitle || `Solicita√ß√£o ${pattern.area}`,
        descricao: this.generateDescriptionTemplate(pattern.area, pattern.tipo, commonPhrases),
        prioridade: mostCommonPriority || 'media',
        confidence: Math.min(pattern.count / 10, 1),
        isAutoGenerated: true,
        createdAt: new Date(),
        usageCount: 0,
        icon: this.getIconForArea(pattern.area),
        tags: [pattern.area, pattern.tipo, 'auto-generated']
      };
      
      return template;
    } catch (error) {
      console.error('‚ùå Erro ao gerar template:', error);
      return null;
    }
  }

  getMostCommon(array) {
    const counts = {};
    array.forEach(item => {
      counts[item] = (counts[item] || 0) + 1;
    });
    
    return Object.entries(counts)
      .sort(([,a], [,b]) => b - a)[0]?.[0];
  }

  getMostCommonPhrase(titles) {
    const words = titles.join(' ').toLowerCase().split(/\s+/);
    const phrases = [];
    
    for (let i = 0; i < words.length - 2; i++) {
      phrases.push(`${words[i]} ${words[i+1]} ${words[i+2]}`);
    }
    
    return this.getMostCommon(phrases);
  }

  generateDescriptionTemplate(area, tipo, commonPhrases) {
    const templates = {
      'comunicacao_visual': {
        'arte_grafica': `Solicita√ß√£o de arte gr√°fica.\n\nTipo: [ESPECIFICAR_TIPO]\nFormato: [DIMENS√ïES]\nPrazo: [DATA_ENTREGA]\n\nDescri√ß√£o detalhada:\n[DETALHES_DA_ARTE]`,
        'impressao': `Solicita√ß√£o de impress√£o.\n\nMaterial: [TIPO_MATERIAL]\nQuantidade: [QTD]\nFormato: [TAMANHO]\nAcabamento: [ESPECIFICAR]`,
        'sinalizacao': `Solicita√ß√£o de sinaliza√ß√£o.\n\nTipo: [PLACA/BANNER/ADESIVO]\nLocal: [ONDE_INSTALAR]\nTexto: [CONTE√öDO]\nDimens√µes: [TAMANHO]`
      },
      'almoxarifado': {
        'pedido_material': `Solicita√ß√£o de material do almoxarifado.\n\nMaterial: [TIPO_MATERIAL]\nQuantidade: [QTD]\nLocal de entrega: [LOCAL]\nPrazo: [DATA_NECESS√ÅRIA]\n\nJustificativa:\n[MOTIVO_DA_SOLICITA√á√ÉO]`,
        'pedido_mobiliario': `Pedido de mobili√°rio.\n\nItem: [ESPECIFICAR_MOBILI√ÅRIO]\nQuantidade: [QTD]\nLocal: [DESTINO]`
      }
    };
    
    return templates[area]?.[tipo] || `Solicita√ß√£o relacionada a ${area} - ${tipo}.\n\nDescri√ß√£o: [ESPECIFICAR]\nPrazo: [DATA]\nObserva√ß√µes: [INFORMA√á√ïES_ADICIONAIS]`;
  }

  extractCommonPhrases(descriptions) {
    const allText = descriptions.join(' ').toLowerCase();
    const phrases = allText.match(/\b\w+\s+\w+\s+\w+\b/g) || [];
    
    const phraseCount = {};
    phrases.forEach(phrase => {
      phraseCount[phrase] = (phraseCount[phrase] || 0) + 1;
    });
    
    return Object.entries(phraseCount)
      .filter(([phrase, count]) => count > 1)
      .sort(([,a], [,b]) => b - a)
      .slice(0, 5)
      .map(([phrase]) => phrase);
  }

  getIconForArea(area) {
    const icons = {
      'comunicacao_visual': 'üé®',
      'producao': 'üîß',
      'almoxarifado': 'üì¶',
      'operacional': '‚öôÔ∏è',
      'logistica': 'üöö',
      'locacao': 'üè¢',
      'compras': 'üõí',
      'financeiro': 'üí∞'
    };
    return icons[area] || 'üìã';
  }

  async saveTemplate(template) {
    try {
      const templateRef = doc(db, this.templatesCollection, template.id);
      await setDoc(templateRef, template);
      console.log('‚úÖ Template salvo:', template.id);
    } catch (error) {
      console.error('‚ùå Erro ao salvar template:', error);
    }
  }

  async recordTemplateUsage(templateId, success = true) {
    try {
      const usageRef = doc(db, 'ai_template_usage', `${templateId}_${Date.now()}`);
      await setDoc(usageRef, {
        templateId,
        success,
        usedAt: new Date(),
        userId: 'current_user'
      });
    } catch (error) {
      console.error('‚ùå Erro ao registrar uso:', error);
    }
  }
}

